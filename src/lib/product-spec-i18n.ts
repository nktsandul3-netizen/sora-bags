/** Normalize dimension/measurement strings for non-Russian locales. */
export function localizeMeasurement(value: string, locale: Locale): string | null {
  if (locale === "ru") return null;

  if (/^До \d/.test(value)) {
    return value.replace(/^До /, locale === "ro" ? "Până la " : "Up to ");
  }

  if (/^≈ \d+ л$/.test(value)) {
    return value.replace(/ л$/, " l");
  }

  if (/^\d+[–-]\d+\s+дней$/.test(value) || /^\d+[–-]\d+\s+дня$/.test(value) || /^\d+\s+дней$/.test(value) || /^\d+\s+дня$/.test(value)) {
    return value
      .replace(/дней$/, locale === "ro" ? "zile" : "days")
      .replace(/дня$/, locale === "ro" ? "zile" : "days");
  }

  if (/^[\d.,]+\s*[×xх][\d.,×xх\s]+$/i.test(value)) {
    return value.replace(/х/g, "×").replace(/x/gi, "×");
  }

  if (/\d/.test(value) && /(?:см|мм|шт\.|л)/.test(value)) {
    return value
      .replace(/х/g, "×")
      .replace(/ см/g, " cm")
      .replace(/ мм/g, " mm")
      .replace(/(\d+)–(\d+) шт\./, (_, a, b) => `${a}–${b} ${locale === "ro" ? "buc." : "pcs."}`)
      .replace(/(\d+) шт\./, (_, a) => `${a} ${locale === "ro" ? "buc." : "pcs."}`);
  }

  return null;
}

export function lookupSpecTranslation(
  value: string | undefined,
  locale: Locale,
  maps: SpecTranslationMap[],
): string | undefined {
  if (!value || locale === "ru") return undefined;
  for (const map of maps) {
    const hit = map[value]?.[locale];
    if (hit) return hit;
  }
  return undefined;
}

